i<-which(stock_pickedT=="CYMI")
j<-which(stock_pickedT=="WWWW")
pair<-dataT[1:dl,i]-1.692988*dataT[1:dl,j]
plot(pair[1:dl],type="l",main=paste(stock_pickedT[i],stock_pickedT[j]),xlab="",ylab="")
v<-sqrt(var(pair[range:dl]))
m<-mean(pair[range:dl])
v1<-sqrt(var(pair[1:dl]))
m1<-mean(pair[1:dl])
abline(m,0,col="green")
abline(m+v,0)
abline(m-v,0)      
abline(m+2*v,0,col="red")
abline(m-2*v,0,col="red")
abline(m1,0,col="blue",lwd=2)
abline(m1+v1,0,lwd=2)
abline(m1-v1,0,lwd=2)      
abline(m1+2*v1,0,col="red",lwd=2)
abline(m1-2*v1,0,col="red",lwd=2)
print(2*v)

